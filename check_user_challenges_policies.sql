-- Check RLS policies on user_challenges that might block team_id
SELECT 
  schemaname,
  tablename,
  policyname,
  permissive,
  roles,
  cmd,
  qual,
  with_check
FROM pg_policies
WHERE tablename = 'user_challenges'
ORDER BY policyname;

-- Check triggers on user_challenges
SELECT 
  trigger_name,
  event_manipulation,
  event_object_table,
  action_statement,
  action_timing
FROM information_schema.triggers
WHERE event_object_table = 'user_challenges'
ORDER BY trigger_name;
